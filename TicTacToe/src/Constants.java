/**
 * This interface defines constants for a tic-tac-toe game.
 */
public interface Constants {
    /**
     * Constant representing an empty space on the game board.
     */
    static final char SPACE_CHAR = ' ';

    /**
     * Constant representing the letter 'O' for one of the players.
     */
    static final char LETTER_O = 'O';

    /**
     * Constant representing the letter 'X' for the other player.
     */
    static final char LETTER_X = 'X';
}
